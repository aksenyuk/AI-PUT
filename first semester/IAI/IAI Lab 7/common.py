import numpy as np
import matplotlib.pyplot as plt

def plotPath(terrain, path):
    terrain_copy = terrain.copy()*2
    for n in path.getPath():
        terrain_copy[n] = 1

    plt.imshow(terrain_copy)
    
    
def plotSteps(terrain,visited):
    terrain_copy = np.zeros(terrain.shape)
    for n in visited:
        terrain_copy[n.position] = n.step

    plt.imshow(terrain_copy)
    
